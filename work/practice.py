#----------- While Loop in Python ------------

i = 1
while i < 6:
  print(i)
  i += 1

#----------- While loop for pring elemenst of list ------------

my_list = [10, 20, 30, 40, 50]   # Define a list

index = 0  # Initialize counter

while index < len(my_list):   # Use while loop to iterate through the list
    print(my_list[index])
    index += 1